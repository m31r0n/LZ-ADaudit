def _svg_donut(counts: dict[str, int], colors: dict[str, str], title: str,
               size: int = 220) -> str:
    total = sum(counts.values()) or 1
    cx = cy = size / 2
    ro, ri = size * 0.38, size * 0.22
    segs: list[str] = []
    legend: list[str] = []
    angle = -math.pi / 2
    for i, (key, val) in enumerate(counts.items()):
        if not val:
            continue
        sweep = 2 * math.pi * val / total
        x1 = cx + ro * math.cos(angle)
        y1 = cy + ro * math.sin(angle)
        x2 = cx + ro * math.cos(angle + sweep)
        y2 = cy + ro * math.sin(angle + sweep)
        xi1 = cx + ri * math.cos(angle + sweep)
        yi1 = cy + ri * math.sin(angle + sweep)
        xi2 = cx + ri * math.cos(angle)
        yi2 = cy + ri * math.sin(angle)
        lg = 1 if sweep > math.pi else 0
        c = colors.get(key, _CAT_COLORS[i % len(_CAT_COLORS)])
        segs.append(
            f'<path d="M{x1:.1f} {y1:.1f} A{ro:.1f} {ro:.1f} 0 {lg} 1 {x2:.1f} {y2:.1f}'
            f' L{xi1:.1f} {yi1:.1f} A{ri:.1f} {ri:.1f} 0 {lg} 0 {xi2:.1f} {yi2:.1f}Z"'
            f' fill="{c}" stroke="#1a2035" stroke-width="1.5"><title>{key}: {val}</title></path>'
        )
        if sweep > 0.28:
            mid = angle + sweep / 2
            lx = cx + ro * 1.18 * math.cos(mid)
            ly = cy + ro * 1.18 * math.sin(mid)
            segs.append(
                f'<text x="{lx:.1f}" y="{ly:.1f}" fill="{c}" font-size="10"'
                f' text-anchor="middle" dominant-baseline="middle" font-family="monospace">'
                f'{val/total*100:.0f}%</text>'
            )
        angle += sweep
        ly_l = 14 + i * 16
        legend.append(
            f'<rect x="0" y="{ly_l-9}" width="10" height="10" fill="{c}" rx="2"/>'
            f'<text x="14" y="{ly_l}" fill="#cdd6f4" font-size="10" font-family="monospace">'
            f'{key} ({val})</text>'
        )
    segs += [
        f'<text x="{cx:.1f}" y="{cy-7:.1f}" fill="#fff" font-size="22"'
        f' text-anchor="middle" font-family="monospace" font-weight="bold">{sum(counts.values())}</text>',
        f'<text x="{cx:.1f}" y="{cy+11:.1f}" fill="#888" font-size="10"'
        f' text-anchor="middle" font-family="monospace">total</text>',
    ]
    tw, th = size + 140, size + 30
    return (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{tw}" height="{th}"'
        f' viewBox="0 0 {tw} {th}" style="background:transparent">'
        f'<text x="{tw/2:.1f}" y="18" fill="#cdd6f4" font-size="13" text-anchor="middle"'
        f' font-family="monospace" font-weight="bold">{title}</text>'
        f'<g transform="translate(0,24)">{"".join(segs)}</g>'
        f'<g transform="translate({size+10},{24+(size-len(counts)*16)//2})">{"".join(legend)}</g>'
        f'</svg>'
    )


def _svg_hbar(counts: dict[str, int], colors: list[str], title: str, width: int = 460) -> str:
    if not counts:
        return ""
    max_v = max(counts.values()) or 1
    bh, gap, lw = 22, 6, 120
    ba = width - lw - 50
    ch = len(counts) * (bh + gap) + 40
    lines = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{ch}"'
        f' viewBox="0 0 {width} {ch}" style="background:transparent">',
        f'<text x="{width/2:.1f}" y="16" fill="#cdd6f4" font-size="13"'
        f' text-anchor="middle" font-family="monospace" font-weight="bold">{title}</text>',
    ]
    for i, (key, val) in enumerate(counts.items()):
        c = colors[i % len(colors)]
        y = 28 + i * (bh + gap)
        bw = int(ba * val / max_v)
        lines += [
            f'<text x="{lw-4}" y="{y+bh-5}" fill="#cdd6f4" font-size="11"'
            f' text-anchor="end" font-family="monospace">{_h(key)}</text>',
            f'<rect x="{lw}" y="{y}" width="{bw}" height="{bh}" fill="{c}" rx="3">'
            f'<title>{key}: {val}</title></rect>',
            f'<text x="{lw+bw+4}" y="{y+bh-5}" fill="{c}" font-size="11"'
            f' font-family="monospace">{val}</text>',
        ]
    lines.append("</svg>")
    return "".join(lines)


