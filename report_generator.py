def _section_privileged_accounts(data: AuditData) -> str:
    def parse_group_members(lines: list[str]) -> list[tuple[str, str]]:
        """Parse `user <sam> <displayname>` lines."""
        result = []
        for ln in lines:
            parts = ln.split(" ", 2)
            if len(parts) >= 2:
                result.append((parts[1], parts[2] if len(parts) > 2 else ""))
        return result

    def parse_account_list(lines: list[str]) -> list[tuple[str, str]]:
        """Parse `<sam> (<displayname>)` or bare lines."""
        result = []
        for ln in lines:
            m = re.match(r"^(\S+)\s+\(([^)]+)\)", ln)
            if m:
                result.append((m.group(1), m.group(2)))
            else:
                result.append((ln.strip(), ""))
        return result

    tabs: list[str] = []
    panels: list[str] = []

    def _add_tab(key: str, title: str, parser: Any) -> None:
        lines = _txt(data, key)
        if not lines:
            return
        parsed = parser(lines)
        active = "active" if not tabs else ""
        idx = len(tabs)
        tabs.append(f'<button class="tab-btn {active}" data-tab="priv-{idx}">{title} ({len(parsed)})</button>')
        rows = "".join(
            f'<tr><td class="mono small">{_h(sam)}</td><td class="small">{_h(dn)}</td></tr>'
            for sam, dn in parsed
        )
        panels.append(
            f'<div class="tab-panel {active}" id="priv-{idx}">'
            f'<table class="data-table"><thead><tr><th>SAM Account</th><th>Display Name</th></tr></thead>'
            f'<tbody>{rows}</tbody></table></div>'
        )

