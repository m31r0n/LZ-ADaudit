def _parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Generate HTML and/or XLSX security report from an AdAudit output folder.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""Examples:
  python report_generator.py                              # auto-detect folder
  python report_generator.py C:\\Audit\\<DC>_<timestamp>  # explicit folder
  python report_generator.py . -f html                   # HTML only
  python report_generator.py . -f xlsx                   # XLSX only
  python report_generator.py . -o myreport --open        # custom name + open
""",
    )
    p.add_argument("folder", nargs="?", default=None,
                   help="AdAudit output folder (auto-detected if omitted)")
    p.add_argument("-f", "--format", choices=["html", "xlsx", "both"], default="both",
                   help="Output format (default: both)")
    p.add_argument("-o", "--output", default=None,
                   help="Base name for output files (no extension; default: 'report')")
    p.add_argument("--open", action="store_true",
                   help="Open HTML report in default browser after generation")
    return p.parse_args()


def main() -> None:
    args = _parse_args()

    # --- Resolve folder ---
    if args.folder:
        folder = Path(args.folder).expanduser().resolve()
    else:
        folder = auto_detect_folder()
        if not folder:
            print("[ERROR] No AdAudit output folder found. Pass a folder path as argument.",
                  file=sys.stderr)
            sys.exit(1)
        print(f"  Auto-detected: {folder}")

    if not folder.is_dir():
        print(f"[ERROR] Not a directory: {folder}", file=sys.stderr)
        sys.exit(1)
    if not (folder / "findings.ndjson").exists():
        print(f"[ERROR] findings.ndjson not found in {folder}", file=sys.stderr)
        sys.exit(1)

    base_name = args.output or "report"
    html_path = folder / f"{base_name}.html"
    xlsx_path = folder / f"{base_name}.xlsx"

    # v1.4.0: refuse silently degrading from "both" → HTML when openpyxl is missing.
    # If the user explicitly asked for XLSX (or both) and openpyxl is unavailable, exit non-zero.
    if args.format in ("xlsx", "both") and not _XLSX_OK:
        print(
            "[ERROR] openpyxl is required for XLSX output but is not installed.\n"
            "        Install with: pip install openpyxl\n"
            "        Or run with -f html to generate only the HTML report.",
            file=sys.stderr,
        )
        if args.format == "xlsx":
            sys.exit(2)
        # For -f both, the user gets a warning and HTML still proceeds, but exit code is 2.

    print(f"\n  Folder : {folder}")
    if args.format in ("html", "both"):
        print(f"  HTML   : {html_path.name}")
    if args.format in ("xlsx", "both"):
        print(f"  XLSX   : {xlsx_path.name}{'  [SKIPPED — openpyxl missing]' if not _XLSX_OK else ''}")
    print()

    # --- Load ---
    data = load_audit_data(folder)
    total = len(data.findings)
    hi = data.summary.get("findings_by_severity", {}).get("high", 0)
    print(f"\n  {total} findings loaded  ({hi} high)\n")

    # --- HTML ---
    if args.format in ("html", "both"):
        print("  Building HTML…", end=" ", flush=True)
        html = build_html(data)
        html_path.write_text(html, encoding="utf-8")
        size_kb = html_path.stat().st_size // 1024
        print(f"done  ({size_kb} KB)")

    # --- XLSX ---
    if args.format in ("xlsx", "both"):
        print("  Building XLSX…", end=" ", flush=True)
        build_xlsx(data, xlsx_path)
        if xlsx_path.exists():
            size_kb = xlsx_path.stat().st_size // 1024
            print(f"done  ({size_kb} KB)")

    print(f"\n  Reports saved to: {folder}\n")

    if args.open and args.format in ("html", "both"):
        webbrowser.open(html_path.as_uri())

    # If user asked for XLSX (alone or via "both") and openpyxl was missing, exit non-zero so
    # CI / scheduled jobs notice the partial result.
    if args.format in ("xlsx", "both") and not _XLSX_OK:
        sys.exit(2)


if __name__ == "__main__":
    main()
