def _h(v: Any) -> str:
    return (str(v)
            .replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;"))


def _fmt_dt(iso: str | None) -> str:
    if not iso:
        return "—"
    try:
        dt = datetime.fromisoformat(iso.replace("Z", "+00:00"))
        return dt.strftime("%Y-%m-%d %H:%M UTC")
    except ValueError:
        return _h(iso)


def _fmt_dur(s: float | int | None) -> str:
    if s is None:
        return "—"
    s = int(s)
    return f"{s // 60}m {s % 60:02d}s" if s >= 60 else f"{s}s"


def _txt(data: AuditData, key: str) -> list[str]:
    return data.txt_files.get(key, [])


def _dfl_label(s: str) -> str:
    m = re.match(r"Windows(\d+)(R2)?(Domain|Forest)", s or "")
    if m:
        return f"Windows Server {m.group(1)}{' R2' if m.group(2) else ''}"
    return s or "—"


def _msdate(s: str | None) -> str:
    m = re.match(r"/Date\((\d+)\)/", s or "")
    if m:
        return datetime.fromtimestamp(int(m.group(1)) / 1000, tz=timezone.utc).strftime("%Y-%m-%d")
    return s or "—"


