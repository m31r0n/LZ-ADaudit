class _PlainProgress:
    def __init__(self, total: int) -> None:
        self._total = total
        self._done = 0

    def __enter__(self) -> "_PlainProgress":
        return self

    def __exit__(self, *_: Any) -> None:
        print()

    def add_task(self, label: str, total: int | None = None) -> int:
        return 0

    def advance(self, task_id: int, advance: int = 1) -> None:
        self._done += advance
        pct = int(self._done / self._total * 100)
        bar = "#" * (pct // 4)
        print(f"\r  [{bar:<25}] {pct:3d}%", end="", flush=True)

    def update(self, task_id: int, description: str = "") -> None:
        pass


def _make_progress(total: int):
    if _RICH:
        return Progress(
            SpinnerColumn(),
            TextColumn("[bold cyan]{task.description}"),
            BarColumn(),
            TaskProgressColumn(),
            TimeElapsedColumn(),
        )
    return _PlainProgress(total)


