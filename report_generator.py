def _read_text(path: Path) -> str:
    raw = path.read_bytes()
    for bom, enc in ((b"\xef\xbb\xbf", "utf-8-sig"), (b"\xff\xfe", "utf-16")):
        if raw.startswith(bom):
            return raw.decode(enc)
    return raw.decode("utf-8", errors="replace")


def _load_json(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    try:
        return json.loads(_read_text(path))
    except (json.JSONDecodeError, UnicodeDecodeError):
        return {}


def _load_ndjson(path: Path) -> list[dict[str, Any]]:
    if not path.exists():
        return []
    records: list[dict[str, Any]] = []
    bad = 0
    for line in _read_text(path).splitlines():
        line = line.strip()
        if line:
            try:
                records.append(json.loads(line))
            except json.JSONDecodeError:
                bad += 1
    if bad:
        # v1.4.0: surface malformed lines instead of silently dropping (was: bare `pass`)
        print(f"  [warn] {path.name}: skipped {bad} malformed NDJSON line(s)", file=sys.stderr)
    return records


def _load_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    # v1.4.0: catch only specific exceptions and report them; previously a blanket
    # `except Exception` swallowed everything, so a corrupt CSV silently produced
    # an empty report.
    try:
        reader = csv.DictReader(_read_text(path).splitlines())
        return [dict(row) for row in reader]
    except (csv.Error, UnicodeDecodeError, OSError) as e:
        print(f"  [warn] {path.name}: failed to parse CSV ({type(e).__name__}: {e})", file=sys.stderr)
        return []


def _load_txt(path: Path) -> list[str]:
    if not path.exists():
        return []
    return [ln for ln in _read_text(path).splitlines() if ln.strip()]


def load_audit_data(folder: Path) -> AuditData:
    """Load every AdAudit output file from *folder* with a progress indicator."""
    data = AuditData(folder=folder)
    prg = _make_progress(_TOTAL_STEPS)

    with prg as p:
        if _RICH:
            task = p.add_task("Loading…", total=_TOTAL_STEPS)

        def step(label: str) -> None:
            if _RICH:
                p.update(task, description=label)
            p.advance(0 if _RICH else 0)

        # --- Core JSON ---
        step("findings.ndjson")
        data.findings = _load_ndjson(folder / "findings.ndjson")

        step("summary.json")
        data.summary = _load_json(folder / "summary.json")

        step("execution.json")
        data.execution = _load_json(folder / "execution.json")

        step("preflight.json")
        data.preflight = _load_json(folder / "preflight.json")
        if not data.preflight:
            data.preflight = data.execution.get("preflight_result", {})

        # --- Evidence JSON ---
        ev_dir = folder / "evidence"
        for stem in _EVIDENCE_STEMS:
            step(f"evidence/{stem}.json")
            j = _load_json(ev_dir / f"{stem}.json")
            if j:
                data.evidence[stem] = j

        # --- Inventory CSV ---
        inv_dir = folder / "inventory"
        for stem in _INVENTORY_STEMS:
            step(f"inventory/{stem}.csv")
            rows = _load_csv(inv_dir / f"{stem}.csv")
            if rows:
                data.inventory[stem] = rows

        # --- Root TXT files ---
        # v1.4.0: prefer execution.json's output_manifest (authoritative list of stems
        # the .ps1 actually generated). Fall back to the hardcoded _TXT_STEMS list for
        # baselines produced by older versions of AdAudit.ps1.
        manifest = data.execution.get("output_manifest", []) or []
        manifest_txt_stems = sorted({
            Path(name).stem for name in manifest
            if isinstance(name, str) and name.lower().endswith(".txt")
        })
        # Union with the hardcoded list so we don't lose stems from older runs.
        all_txt_stems = sorted(set(_TXT_STEMS) | set(manifest_txt_stems))
        for stem in all_txt_stems:
            step(f"{stem}.txt")
            lines = _load_txt(folder / f"{stem}.txt")
            if lines:
                data.txt_files[stem] = lines

        # --- Root CSV ---
        step("security_events.csv")
        data.security_events_csv = _load_csv(folder / "security_events.csv")

        step("findings.csv")
        data.findings_csv = _load_csv(folder / "findings.csv")

        # --- Companion files ---
        data.gpo_report_exists = (folder / "GPOReport.html").exists()

    return data


def auto_detect_folder() -> Path | None:
    """Scan cwd for the most recent AdAudit output folder."""
    cwd = Path.cwd()
    if (cwd / "findings.ndjson").exists():
        return cwd
    candidates = sorted(
        [d for d in cwd.iterdir() if d.is_dir() and (d / "findings.ndjson").exists()],
        key=lambda d: d.stat().st_mtime,
        reverse=True,
    )
    if not candidates:
        return None
    if len(candidates) > 1:
        print("  Multiple audit folders found — using the most recent:")
        for c in candidates[:5]:
            print(f"    {c.name}")
        print()
    return candidates[0]


